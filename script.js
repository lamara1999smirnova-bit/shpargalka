    // ========== ИСПРАВЛЕННАЯ ФУНКЦИЯ КОПИРОВАНИЯ ==========
    function copyToClipboard(text) {
        // Создаем временный текстовый элемент
        const tempInput = document.createElement('textarea');
        tempInput.value = text;
        tempInput.style.position = 'fixed';
        tempInput.style.opacity = '0';
        tempInput.style.pointerEvents = 'none';
        tempInput.style.left = '0';
        tempInput.style.top = '0';
        document.body.appendChild(tempInput);
        
        // Выделяем и копируем
        tempInput.focus();
        tempInput.select();
        tempInput.setSelectionRange(0, 99999); // Для мобильных устройств
        
        try {
            // Пробуем современный API сначала
            navigator.clipboard.writeText(text).then(() => {
                showNotification(text);
            }).catch(() => {
                // Если не сработало, используем execCommand
                document.execCommand('copy');
                showNotification(text);
            });
        } catch (err) {
            // Финальный fallback
            document.execCommand('copy');
            showNotification(text);
        }
        
        // Удаляем временный элемент
        document.body.removeChild(tempInput);
    }

    // Функция показа уведомления
    function showNotification(text) {
        const notification = document.createElement('div');
        notification.className = 'copy-notification';
        
        // Обрезаем длинный текст для уведомления
        const displayText = text.length > 30 ? text.substring(0, 30) + '...' : text;
        notification.textContent = `✅ Скопировано: ${displayText}`;
        
        document.body.appendChild(notification);
        
        setTimeout(() => {
            notification.remove();
        }, 2000);
    }

    // ========== СОЗДАНИЕ КОЛОНОК ==========
    function createColumns() {
        const container = document.getElementById('empathy-columns');
        if (!container) return;
        
        container.innerHTML = '';
        
        const titles = [
            'Начинаем диалог',
            'Понимание/сочувствие',
            'Продолжаем диалог',
            'Извинения',
            'Активная работа',
            'Завершение'
        ];
        
        for (let i = 1; i <= 6; i++) {
            const column = document.createElement('div');
            column.className = 'column';
            
            const header = document.createElement('div');
            header.className = `column-header col${i}-header`;
            header.textContent = titles[i-1];
            
            const list = document.createElement('ul');
            list.className = 'phrases-list';
            list.id = `col${i}-list`;
            
            templates[`col${i}`].forEach(phrase => {
                const li = document.createElement('li');
                li.textContent = phrase;
                li.addEventListener('click', () => copyToClipboard(phrase));
                list.appendChild(li);
            });
            
            column.appendChild(header);
            column.appendChild(list);
            container.appendChild(column);
        }
    }

    // ========== СОЗДАНИЕ СИМВОЛОВ ==========
    function createSymbols() {
        const grid = document.getElementById('symbols-grid');
        if (!grid) return;
        
        grid.innerHTML = '';
        
        symbols.forEach(symbol => {
            const btn = document.createElement('button');
            btn.className = 'symbol-btn';
            btn.textContent = symbol.char;
            btn.setAttribute('aria-label', `Скопировать символ ${symbol.name}`);
            btn.addEventListener('click', () => copyToClipboard(symbol.char));
            grid.appendChild(btn);
        });
    }

    // ========== ПЕРЕКЛЮЧЕНИЕ ВКЛАДОК ==========
    function switchTab(tab) {
        const panels = {
            empathy: document.getElementById('panel-empathy'),
            symbols: document.getElementById('panel-symbols')
        };
        
        const btns = {
            empathy: document.getElementById('tab-empathy'),
            symbols: document.getElementById('tab-symbols')
        };
        
        // Скрываем все панели и убираем активные классы
        Object.values(panels).forEach(p => p?.classList.remove('active'));
        Object.values(btns).forEach(b => b?.classList.remove('active'));
        
        // Показываем выбранную
        if (tab === 'empathy') {
            panels.empathy?.classList.add('active');
            btns.empathy?.classList.add('active');
        } else {
            panels.symbols?.classList.add('active');
            btns.symbols?.classList.add('active');
        }
    }

    // ========== ИНИЦИАЛИЗАЦИЯ ==========
    createColumns();
    createSymbols();
    
    // Обработчики вкладок
    document.getElementById('tab-empathy')?.addEventListener('click', () => switchTab('empathy'));
    document.getElementById('tab-symbols')?.addEventListener('click', () => switchTab('symbols'));
    
    console.log('✅ Шпаргалка готова!');
